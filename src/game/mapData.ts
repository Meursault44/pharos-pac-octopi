export const MAP_COLS = 27;
export const MAP_ROWS = 14;

export const RAW_LAYOUT: string[] = [
    '###########################',
    '#..o.................o....#',
    '#..#.####....###...####...#',
    '#....#....##.#...#.#.##.#.#',
    '#.##.#.# .##.#.###...##.#.#',
    '#.# .#.##.# .#.#.#.#. #.#.#',
    '#.##...##.##.#.#.#.#....#.#',
    '#....#####...### ..#####..#',
    '#........... ..........o..#',
    '#.#######....###...#####..#',
    '#....#....##.#...#.#....#.#',
    '#.##.#.##.##.#.#.#.#.##.#.#',
    '#o.....#................o.#',
    '###########################',
];

export const isWallAt = (col: number, row: number) =>
    row >= 0 &&
    row < MAP_ROWS &&
    col >= 0 &&
    col < MAP_COLS &&
    RAW_LAYOUT[row][col] === '#';

export const cellKey = (c: number, r: number) => `${c},${r}`;